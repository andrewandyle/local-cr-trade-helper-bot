package com.gleipnirymir.exceptions;

public class CrthException extends Exception {

    public CrthException() {
        super();
    }

    public CrthException(String message) {
        super(message);
    }

    public CrthException(String message, Throwable cause) {
        super(message, cause);
    }

    public CrthException(Throwable cause) {
        super(cause);
    }
}
